#ifndef __CONFIG_DATA_H__
#define __CONFIG_DATA_H__

#define PARAM_NAME_SIZE 64
#define PARAM_VALUE_SIZE 64
#define PARAM_DATA_SIZE 128

//////////////////////////////////////////////////////////////////////////////
struct ParamData
{
    char param_name[PARAM_NAME_SIZE];
    char param_value[PARAM_VALUE_SIZE];
};

//////////////////////////////////////////////////////////////////////////////
struct ConfigData
{
public:
    ConfigData(const char * file_name);
    
    ParamData params[PARAM_DATA_SIZE]; 
};

#endif //__CONFIG_DATA_H__
